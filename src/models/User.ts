import mongoose, { Schema } from "mongoose";
import { IUserModel } from "../interfaces/IUser";

const UserSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  username: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  type: {
    type: String,
    enum: ["Admin"],
  },
});

export default mongoose.model<IUserModel>("User", UserSchema);
